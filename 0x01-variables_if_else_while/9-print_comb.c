#include <stdio.h>
/**
 * main - function declared to print all combinations of a sing
 * le digit number
 * Return: always set to 0
 */

int main(void)
{
	int n;

	for (n = 0; n <= 9; n++)
	{
		putchar((n % 10) + '0');
		if (n == 9)
			continue;
		putchar(',');
		putchar(' ');
	}
	putchar('\n');
	return (0);
}
